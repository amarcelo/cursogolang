package main

import (
	"fmt"
	"sync"
	"time"
)

////////////////////////////////////////////////////////////
// Contador global de pedidos
////////////////////////////////////////////////////////////

var totalPedidos int

////////////////////////////////////////////////////////////
// Mutex protege o contador compartilhado
////////////////////////////////////////////////////////////

var mu sync.Mutex

////////////////////////////////////////////////////////////
// Once garante que o banco será iniciado apenas uma vez
////////////////////////////////////////////////////////////

var once sync.Once

////////////////////////////////////////////////////////////
// Simula inicialização do banco
////////////////////////////////////////////////////////////

func inicializarBanco() {

	fmt.Println("Conectando ao banco de dados...")

	time.Sleep(2 * time.Second)

	fmt.Println("Banco conectado com sucesso!")
}

////////////////////////////////////////////////////////////
// Processa um pedido
////////////////////////////////////////////////////////////

func processarPedido(cliente int, wg *sync.WaitGroup) {

	// Informa ao WaitGroup que a tarefa terminou
	defer wg.Done()

	////////////////////////////////////////////////////////
	// Inicializa o banco apenas uma única vez
	////////////////////////////////////////////////////////

	once.Do(inicializarBanco)

	fmt.Println("Cliente", cliente, "enviou um pedido.")

	////////////////////////////////////////////////////////
	// Simula tempo de processamento
	////////////////////////////////////////////////////////

	time.Sleep(time.Second)

	////////////////////////////////////////////////////////
	// Região crítica
	////////////////////////////////////////////////////////

	mu.Lock()

	defer mu.Unlock()

	totalPedidos++

	fmt.Println("Pedido do cliente", cliente,
		"processado. Total:", totalPedidos)
}

////////////////////////////////////////////////////////////

func main() {

	var wg sync.WaitGroup

	////////////////////////////////////////////////////////
	// Simula 20 clientes acessando juntos
	////////////////////////////////////////////////////////

	for i := 1; i <= 20; i++ {

		wg.Add(1)

		go processarPedido(i, &wg)
	}

	////////////////////////////////////////////////////////
	// Espera todos terminarem
	////////////////////////////////////////////////////////

	wg.Wait()

	fmt.Println()
	fmt.Println("=================================")
	fmt.Println("Todos os pedidos foram concluídos")
	fmt.Println("Quantidade final:", totalPedidos)
}
